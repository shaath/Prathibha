package com.SeleniumTraining.javaProgrammes;

public class SwitchCaseEg2 {

	public static void main(String[] args) {
		int a = 10;
		
		switch(a){
		case 1:
			System.out.println("Jan");
			break;
		case 2:
			System.out.println("Feb");
			break;
		case 10:
			System.out.println("Oct");
			break;
		}

	}

}
